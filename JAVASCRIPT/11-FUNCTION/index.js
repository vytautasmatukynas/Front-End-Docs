// Define a basic function named 'greet'
function greet() {
    // Function body: Log a greeting message
    console.log("Hello, world!");
}

// Call the 'greet' function
greet();


// Define a function named 'addNumbers' that takes two parameters
function addNumbers(a, b) {
    // Function body: Calculate the sum of 'a' and 'b'
    let sum = a + b;

    // Return the calculated sum
    return sum;
}

// Call the 'addNumbers' function with arguments 5 and 3
let result = addNumbers(5, 3);

// Log the result to the console
console.log("Sum:", result);
