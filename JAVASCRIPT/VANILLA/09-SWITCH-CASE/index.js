let number = 3;

switch (number) {
    case 1:
        document.getElementById("someText").innerHTML = "ONE";
        break;
    case 2:
        document.getElementById("someText").innerHTML = "TWO";
        break;
    case 3:
        document.getElementById("someText").innerHTML = "THREE";
        break;
    case 4:
        document.getElementById("someText").innerHTML = "FOUR";
        break;
    default:
        document.getElementById("someText").innerHTML = "None";
}
